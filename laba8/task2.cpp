#include <iostream>
#include <fstream>
#include <vector>
#include <string>
#include <algorithm>

using namespace std;

int main() {
    // Открываем входной файл для чтения
    ifstream inputFile("input2.txt");
    // Открываем выходной файл для записи
    ofstream outputFile("output.txt");
    
    // Проверяем, успешно ли открылись файлы
    if (!inputFile.is_open() || !outputFile.is_open()) {
        cerr << "Ошибка открытия файлов!" << endl;
        return 1;
    }
    
    int N;
    inputFile >> N; // Считываем размер доски
    
    // Создаем двумерный вектор для хранения доски с неприятностями
    vector<vector<int>> board(N, vector<int>(N));
    
    // Считываем доску из файла
    for (int i = 0; i < N; i++) {
        for (int j = 0; j < N; j++) {
            inputFile >> board[i][j];
        }
    }
    
    // Создаем DP таблицу для хранения минимальных сумм неприятностей
    // dp[i][j] - минимальная сумма на пути от правого верхнего угла до (i,j)
    vector<vector<int>> dp(N, vector<int>(N));
    
    // Начинаем с правого верхнего угла (0, N-1)
    dp[0][N-1] = board[0][N-1];
    
    // Заполняем верхнюю строку (можно двигаться только влево)
    for (int j = N-2; j >= 0; j--) {
        dp[0][j] = dp[0][j+1] + board[0][j];
    }
    
    // Заполняем правый столбец (можно двигаться только вниз)
    for (int i = 1; i < N; i++) {
        dp[i][N-1] = dp[i-1][N-1] + board[i][N-1];
    }
    
    // Заполняем остальные ячейки таблицы
    for (int i = 1; i < N; i++) {
        for (int j = N-2; j >= 0; j--) {
            // В клетку (i,j) можно попасть слева (i, j+1) или сверху (i-1, j)
            dp[i][j] = min(dp[i][j+1], dp[i-1][j]) + board[i][j];
        }
    }
    
    // Результат хранится в левом нижнем углу (N-1, 0)
    int minSum = dp[N-1][0];
    outputFile << minSum << endl;
    
    // Восстанавливаем путь
    string path;
    int i = N-1, j = 0; // Начинаем с левого нижнего угла и идем обратно
    
    // Идем обратно до правого верхнего угла
    while (i != 0 || j != N-1) {
        // Проверяем, можем ли мы прийти из клетки справа
        if (j + 1 < N && i - 1 >= 0) {
            // Если можно прийти и справа, и сверху, выбираем то направление,
            // которое соответствует минимальному пути в DP
            if (dp[i][j+1] < dp[i-1][j]) {
                path = 'L' + path; // Пришли справа, значит двигались влево
                j++;
            } else {
                path = 'D' + path; // Пришли сверху, значит двигались вниз
                i--;
            }
        }
        // Если мы на верхней границе (i == 0), можем прийти только справа
        else if (i == 0) {
            path = 'L' + path;
            j++;
        }
        // Если мы на правой границе (j == N-1), можем прийти только сверху
        else {
            path = 'D' + path;
            i--;
        }
    }
    
    // Записываем путь в выходной файл
    outputFile << path << endl;
    
    // Закрываем файлы
    inputFile.close();
    outputFile.close();
    
    return 0;
}